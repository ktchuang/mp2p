package edu.bonn.cs.iv.bonnmotion;

public class ScenarioLinkException extends Exception {
	private static final long serialVersionUID = -8304483737139210546L;

	public ScenarioLinkException() {
		super();
	}

	public ScenarioLinkException(String message) {
		super(message);
	}

	public ScenarioLinkException(String message, Throwable cause) {
		super(message, cause);
	}

	public ScenarioLinkException(Throwable cause) {
		super(cause);
	}

}
